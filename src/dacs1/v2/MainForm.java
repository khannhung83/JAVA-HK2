    private void btnSendFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendFileActionPerformed
    String receiver = txtReceiver.getText().trim();
        if (selectedFile == null || receiver.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn file và nhập người nhận");
            return;
        }

        btnSendFile.setEnabled(false); // Disable button to prevent multiple clicks
        SwingWorker<String, Void> worker = new SwingWorker<String, Void>() {
            @Override
            protected String doInBackground() {
                System.out.println("Sending file: " + selectedFile.getName() + " to " + receiver);
                return ClientSocketManager.sendFile(selectedFile, currentUsername, receiver);
            }

            @Override
            protected void done() {
                try {
                    String response = get();
                    if ("FILE_RECEIVED".equals(response)) {
                        JOptionPane.showMessageDialog(MainForm.this, "Gửi file thành công!");
                        refreshFileList();
                    } else {
                        JOptionPane.showMessageDialog(MainForm.this, "Gửi file thất bại: " + response);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(MainForm.this, "Lỗi khi gửi file: " + e.getMessage());
                    e.printStackTrace();
                } finally {
                    btnSendFile.setEnabled(true); // Re-enable button
                }
            }
        };
        worker.execute();
    }//GEN-LAST:event_btnSendFileActionPerformed

    private void btnDownloadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDownloadActionPerformed
    int row = tblFiles.getSelectedRow();
        if (row >= 0) {
            String filename = (String) tblFiles.getValueAt(row, 0);
            String sender = (String) tblFiles.getValueAt(row, 1);
            ClientSocketManager.downloadFile(filename, sender, currentUsername);
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một file để tải về");
        }    // TODO add your handling code here:
    }//GEN-LAST:event_btnDownloadActionPerformed

