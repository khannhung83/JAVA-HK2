    private void refreshFileList() {
        try {
            System.out.println("Làm mới danh sách file cho: " + currentUsername);
            List<FileRecord> records = ClientSocketManager.getFilesForUser(currentUsername, currentUserRole);
            System.out.println("Nhận " + records.size() + " bản ghi file");
            for (FileRecord r : records) {
                System.out.println("File: " + r.filename + ", Gửi: " + r.sender + ", Nhận: " + r.receiver);
            }
            DefaultTableModel model = (DefaultTableModel) tblFiles.getModel();
            model.setRowCount(0);
            for (FileRecord r : records) {
                model.addRow(new Object[]{r.filename, r.sender, r.receiver, r.size, r.uploadTime});
            }
            tblFiles.repaint();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi tải danh sách file: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
    
